package com.example.liuzijia.entity;

public class EntityProperties {
    public String def;
    public String features;
    public String include;
    public String livingRequirement;
    public String spreadMethod;
    public String application;
    public EntityProperties(String def, String features, String include, String livingRequirement,
                            String spreadMethod, String application) {
        this.def = def;
        this.features = features;
        this.include = include;
        this.livingRequirement = livingRequirement;
        this.spreadMethod = spreadMethod;
        this.application = application;
    }

}
